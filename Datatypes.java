class DataTypes{
	public static void main(String[] args ){
		byte a=10;
		short s=55;
		int i=132;
		long l=566;
		System.out.println(a);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		
		char c='A';
		System.out.println(c);
		float f=12.3f;
		System.out.println(f);
		double d=55.3;
		boolean b1=true;
		boolean b2=false;
		System.out.println(b1);
		System.out.println(b2);
	}
}